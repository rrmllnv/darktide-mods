local command_wheel_settings = {
	anim_speed = 25,
	-- Радиусы расположения кнопок (расстояние от центра до центра иконки)
	-- max_radius - внешний радиус (внешняя граница сегментов)
	-- min_radius - внутренний радиус (внутренняя граница сегментов, определяет размер центрального круга с тенью)
	-- Разница (max_radius - min_radius) определяет радиальную глубину сегментов
	-- Для большего центрального круга увеличьте min_radius и center_circle_size
	-- Для большего общего размера увеличьте оба радиуса пропорционально
	max_radius = 300,
	min_radius = 280,
	-- Размер центрального круга с тенью (в пикселях)
	-- Увеличьте это значение, чтобы сделать центральный круг больше
	center_circle_size = 450,
	-- Размер ромба (rhombus), который появляется при наведении на кнопку
	-- По умолчанию пропорционален center_circle_size (ширина = center_circle_size, высота = center_circle_size * 0.256)
	-- Можно настроить отдельно для более точного контроля размера ромба
	-- Черная подложа (middle_circle) управляется через center_circle_size
	rhombus_width = nil,  -- nil = использовать center_circle_size
	rhombus_height = nil,  -- nil = использовать center_circle_size * 0.256
	-- Параметры активации hover (наведение мыши на кнопки)
	-- hover_min_distance - минимальное расстояние от центра экрана для активации hover (в пикселях, с учетом scale)
	-- По умолчанию пропорционально center_circle_size (оригинал был 130 при center_circle_size = 250)
	-- Увеличьте это значение, если ромб появляется слишком рано
	hover_min_distance = nil,  -- nil = использовать center_circle_size * 0.52 (пропорционально оригиналу)
	-- hover_angle_degrees - угловая ширина зоны hover для каждой кнопки (в градусах)
	-- Чем больше значение, тем шире зона активации hover
	hover_angle_degrees = 30,
	-- scan_delay - задержка между сканированием сегментов (в секундах)
	-- Увеличьте это значение, если сегменты появляются слишком быстро
	scan_delay = 0.1,
	wheel_slots = 13,
	-- Размеры сегментов радиальных кнопок (ширина, высота)
	-- slice_width - угловая ширина сегмента (влияет на ширину кнопок и промежутки между ними)
	-- Чем меньше slice_width, тем уже кнопки и больше промежутки между ними
	-- Чем больше slice_width, тем шире кнопки и меньше промежутки (может привести к перекрытию)
	-- slice_height - радиальная глубина сегмента (влияет на кривизну внутренней и внешней границ)
	-- Чем больше slice_height, тем более изогнуты границы (больше радиальная глубина)
	-- Чем меньше slice_height, тем менее изогнуты границы (меньше радиальная глубина)
	-- Для геометрически правильного круга важно сохранить соотношение slice_width/slice_height ≈ 1.36
	-- Оригинальное соотношение: 190/140 ≈ 1.357
	-- Текущее соотношение: 200/147 ≈ 1.360 (сохранено для правильной геометрии)
	slice_width = 150,
	slice_height = 250,
	-- Корректировка кривизны сегментов (независимо от размеров)
	-- slice_curvature_scale_x - масштаб кривизны по ширине (1.0 = без изменений, >1.0 = более изогнуто, <1.0 = менее изогнуто)
	-- slice_curvature_scale_y - масштаб кривизны по высоте (1.0 = без изменений, >1.0 = более изогнуто, <1.0 = менее изогнуто)
	-- Эти параметры изменяют визуальную кривизну без изменения логических размеров кнопок
	-- Для уменьшения кривизны уменьшите slice_curvature_scale_y (например, 0.7-0.9)
	-- Для увеличения кривизны увеличьте slice_curvature_scale_y (например, 1.1-1.3)
	slice_curvature_scale_x = 1.1,
	slice_curvature_scale_y = 0.4,
}

return settings("CommandWheelSettings", command_wheel_settings)
