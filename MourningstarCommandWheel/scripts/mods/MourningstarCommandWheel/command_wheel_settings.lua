local command_wheel_settings = {
	anim_speed = 25,
	-- Радиусы расположения кнопок (расстояние от центра до центра иконки)
	-- При изменении slice_width рекомендуется пропорционально изменить радиусы:
	-- новый_радиус = старый_радиус * (новый_slice_width / старый_slice_width)
	-- Например: 190 * (150/190) = 150
	max_radius = 190,
	min_radius = 185,
	scan_delay = 0.2,
	wheel_slots = 12,
	-- Размеры сегментов радиальных кнопок (ширина, высота)
	-- slice_width - угловая ширина сегмента (влияет на ширину кнопок и промежутки между ними)
	-- Чем меньше slice_width, тем уже кнопки и больше промежутки между ними
	-- Чем больше slice_width, тем шире кнопки и меньше промежутки (может привести к перекрытию)
	-- Для равномерных промежутков рекомендуется уменьшить slice_width (например, 130-140)
	-- slice_height - радиальная глубина сегмента (влияет на кривизну внутренней и внешней границ)
	-- Чем больше slice_height, тем более изогнуты границы (больше радиальная глубина)
	-- Чем меньше slice_height, тем менее изогнуты границы (меньше радиальная глубина)
	-- Примечание: в радиальном меню промежутки физически не могут быть одинаковыми по всей длине
	-- (они всегда сужаются к центру из-за геометрии круга), но можно сделать их визуально более равномерными
	-- Соотношение сторон: 150/105 ≈ 1.429 (оригинальное соотношение было 190/140 ≈ 1.357)
	-- При изменении этих значений рекомендуется также изменить max_radius и min_radius пропорционально
	slice_width = 150,
	slice_height = 105,
}

return settings("CommandWheelSettings", command_wheel_settings)
